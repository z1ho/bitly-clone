// #SUBMIT
$(document).ready(function(){
    $("#submit").mouseenter(function(){
        $(this).css({
        	"background-color": "#bfbfbf",
        	"color": "white",
        	"border": "dotted 3px white"
        });
    });
    $("#submit").mouseleave(function(){
        $(this).css({
        	"background-color": "transparent",
        	"color": "white",
        	"border": "dotted 3px white"
        });
    });    
});

